using System;
using System.Collections;
using System.Text;
using System.Linq;
namespace Numbers_N_one










{
    class Program
    {
        static void Main(string[] args)
        {
            int n = int.Parse(Console.ReadLine());
            for (int i = n; i >= 1; i--)
            {
                Console.WriteLine(i);
            }
        }
    }
}